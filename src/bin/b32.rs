fn main() {
    input! {
        N: usize,
        K: usize,
        A: [usize; K],
    }

    let ans = solve(N, A);
    println!("{}", if ans {"First"} else {"Second"});
}

fn solve(N: usize, A: Vec<usize>) -> bool {
    let mut dp = vec![false; N + 1];
    for i in 0..=N {
        for &a in &A {
            if i.checked_sub(a).is_some() && !dp[i - a] {
                dp[i] = true
            }
        }
    }
    dp[N]
}
